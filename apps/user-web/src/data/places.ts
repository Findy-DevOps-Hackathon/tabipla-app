/** 全地名（都道府県 + 市区町村）をフラットにした一覧。 */
export const PLACES: Place[] = [
  ...PREFECTURES.map(([name, reading]) => ({ name, reading, prefecture: null })),
  ...Object.entries(MUNICIPALITIES).flatMap(([prefecture, list]) =>
    list.map(([name, reading]) => ({ name, reading, prefecture })),
  ),
];

/**
 * 入力文字列に一致する実在地名を返す。
 * 名称・読み・都道府県名のいずれかに含まれていれば候補とし、
 * 前方一致を優先して並べる。
 */
export function searchPlaces(query: string, limit = 8): Place[] {
  const q = query.trim();
  if (q === "") return [];

  const matched = PLACES.filter(
    (p) =>
      p.name.includes(q) ||
      p.reading.includes(q) ||
      (p.prefecture?.includes(q) ?? false),
  );

  return matched
    .sort((a, b) => {
      const aPrefix = a.name.startsWith(q) || a.reading.startsWith(q);
      const bPrefix = b.name.startsWith(q) || b.reading.startsWith(q);
      if (aPrefix !== bPrefix) return aPrefix ? -1 : 1;
      return a.name.length - b.name.length;
    })
    .slice(0, limit);
}
